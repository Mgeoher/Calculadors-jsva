package calculadora;

public class Suma {
    
    
    
    public double sumar(double pnumero, double snumero){
        return pnumero+snumero;
        
}
     public double restar(double pnumero, double snumero){
        return pnumero-snumero;
        
}
         public double multiplicar (double pnumero, double snumero){
        return pnumero*snumero;
        
}
              public double dividir (double pnumero, double snumero){
        return pnumero/snumero;
        
}

}
     
